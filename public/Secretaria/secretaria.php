                <h2>Procesos de Inscripción y Reinscripción</h2>
                <h3>documentos que se pueden emitir</h3>
                <ul>
                    <li>Acta de nacimiento</li>
                    <li>Récord de notas del año anterior</li>
                    <li>Formulario de inscripción completo</li>
                    <li>Copia de identificación del padre o tutor</li>
                </ul>
                <h3>Pasos a seguir para el proceso de inscripción y reinscripción:</h3>
                <ol>
                    <li>Reunir todos los documentos requeridos.</li>
                    <li>Completar el formulario de inscripción o reinscripción.</li>
                    <li>Presentar la documentación en el Departamento de Secretaría.</li>
                    <li>Realizar el pago de la tarifa de inscripción, si aplica.</li>
                    <li>Confirmar la inscripción con el recibo entregado por el departamento.</li>
                </ol>
                <h3>Fechas importantes relacionadas con la inscripción y la reinscripción:</h3>
                <ul>
                    <li>Inscripción de nuevo ingreso: del 1 al 15 de agosto.</li>
                    <li>Reinscripción: del 1 al 15 de julio.</li>
                </ul>
            </article>
            <article id="archivo-registro">
                <h2>Archivo y Registro</h2>
                <p><strong>Explicación sobre el proceso de archivo de expedientes y registros:</strong> El Departamento
                    de Secretaría se encarga de archivar y mantener todos los expedientes y registros académicos de los
                    estudiantes, asegurando que toda la información esté organizada y sea fácilmente accesible cuando se
                    necesite.</p>
                <h3>Información sobre cómo solicitar registros de grado:</h3>
                <ol>
                    <li>Visitar el Departamento de Secretaría.</li>
                    <li>Completar el formulario de solicitud de registros.</li>
                    <li>Proporcionar la información necesaria (nombre completo, grado, año escolar).</li>
                    <li>Pagar la tarifa correspondiente, si aplica.</li>
                    <li>El registro solicitado estará disponible en un plazo de 2 días laborables.</li>
                </ol>
                <h3>Política de privacidad y seguridad de la información archivada:</h3>
                <p>El departamento asegura la confidencialidad y seguridad de todos los registros académicos mediante
                    estrictas políticas de privacidad y el uso de tecnología de encriptación avanzada. Solo el personal
                    autorizado tiene acceso a la información archivada.</p>
